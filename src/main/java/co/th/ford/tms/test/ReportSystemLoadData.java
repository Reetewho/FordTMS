package co.th.ford.tms.test;

public class ReportSystemLoadData {
	
	 String systemLoadID;
	 String loadID;
	 String loadDate;
	 String loadStatus;
	 String countLoadStop;
	
	 
	public String getSystemLoadID() {
		return systemLoadID;
	}
	public void setSystemLoadID(String systemLoadID) {
		this.systemLoadID = systemLoadID;
	}
	public String getLoadID() {
		return loadID;
	}
	public void setLoadID(String loadID) {
		this.loadID = loadID;
	}
	public String getLoadDate() {
		return loadDate;
	}
	public void setLoadDate(String loadDate) {
		this.loadDate = loadDate;
	}
	public String getLoadStatus() {
		return loadStatus;
	}
	public void setLoadStatus(String loadStatus) {
		this.loadStatus = loadStatus;
	}
	public String getCountLoadStop() {
		return countLoadStop;
	}
	public void setCountLoadStop(String countLoadStop) {
		this.countLoadStop = countLoadStop;
	}
}
